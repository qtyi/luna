    private List<Field> GetNodeOrNodeListFields(SyntaxTreeType node)
        => node is AbstractNode an
            ? an.Fields.Where(n => IsNodeOrNodeList(n.Type)).ToList()
            : node is Node nd
                ? nd.Fields.Where(n => IsNodeOrNodeList(n.Type)).ToList()
                : new List<Field>();

    private void WriteRedType(SyntaxTreeType node)
    {
        this.WriteComment(node.TypeComment, "");

        if (node is AbstractNode)
        {
            var nd = (AbstractNode)node;
            this.WriteLine($"public abstract partial class {node.Name} : {node.Base}");
            this.OpenBlock();
            this.WriteLine($"internal {node.Name}(InternalSyntax.{LanguageNames.This}SyntaxNode green, {LanguageNames.This}SyntaxNode? parent, int position)");
            this.WriteLine("  : base(green, parent, position)");
            this.OpenBlock();
            this.CloseBlock();

            var valueFields = nd.Fields.Where(n => !IsNodeOrNodeList(n.Type)).ToList();
            var nodeFields = GetNodeOrNodeListFields(nd);

            foreach (var field in nodeFields)
            {
                if (IsNodeOrNodeList(field.Type))
                {
                    //red SyntaxLists can't contain tokens, so we switch to SyntaxTokenList
                    var fieldType = GetRedFieldType(field);
                    this.WriteLine();
                    this.WriteComment(field.PropertyComment, "");
                    this.WriteLine($"{"public"} abstract {(field.IsNew() ? "new " : "")}{fieldType} {field.Name} {{ get; }}");
                    this.WriteLine($"public {node.Name} With{field.Name}({fieldType} {CamelCase(field.Name)}) => With{field.Name}Core({CamelCase(field.Name)});");
                    this.WriteLine($"internal abstract {node.Name} With{field.Name}Core({fieldType} {CamelCase(field.Name)});");

                    if (field.Type.IsAnyList())
                    {
                        var argType = GetElementType(field.Type);
                        this.WriteLine();
                        this.WriteLine($"public {node.Name} Add{field.Name}(params {argType}[] items) => Add{field.Name}Core(items);");
                        this.WriteLine($"internal abstract {node.Name} Add{field.Name}Core(params {argType}[] items);");
                    }
                    else
                    {
                        var referencedNode = TryGetNodeForNestedList(field);
                        if (referencedNode is not null)
                        {
                            foreach (var referencedNodeField in referencedNode.Fields)
                            {
                                if (referencedNodeField.Type.IsAnyList())
                                {
                                    var argType = GetElementType(referencedNodeField.Type);

                                    this.WriteLine();
                                    this.WriteLine($"public {node.Name} Add{StripPost(field.Name, "Opt")}{referencedNodeField.Name}(params {argType}[] items) => Add{StripPost(field.Name, "Opt")}{referencedNodeField.Name}Core(items);");
                                    this.WriteLine($"internal abstract {node.Name} Add{StripPost(field.Name, "Opt")}{referencedNodeField.Name}Core(params {argType}[] items);");
                                }
                            }
                        }
                    }
                }
            }

            foreach (var field in valueFields)
            {
                this.WriteLine();
                this.WriteComment(field.PropertyComment, "");
                this.WriteLine($"{"public"} abstract {(field.IsNew() ? "new " : "")}{field.Type} {field.Name} {{ get; }}");
            }

            var baseType = GetTreeType(node.Base);
            if (baseType is not null)
            {
                var baseNodeFields = GetNodeOrNodeListFields(baseType);
                if (baseNodeFields.Count > 0)
                {
                    this.WriteLine();
                }

                foreach (var baseField in baseNodeFields)
                {
                    this.WriteLine($"public new {node.Name} With{baseField.Name}({GetRedFieldType(baseField)} {CamelCase(baseField.Name)}) => ({node.Name})With{baseField.Name}Core({CamelCase(baseField.Name)});");
                }

                foreach (var baseField in baseNodeFields)
                {
                    if (baseField.Type.IsAnyList())
                    {
                        var argType = GetElementType(baseField.Type);
                        this.WriteLine();
                        this.WriteLine($"public new {node.Name} Add{baseField.Name}(params {argType}[] items) => ({node.Name})Add{baseField.Name}Core(items);");
                    }
                    else
                    {
                        var referencedNode = TryGetNodeForNestedList(baseField);
                        if (referencedNode is not null)
                        {
                            // look for list members...
                            foreach (var referencedNodeField in referencedNode.Fields)
                            {
                                if (referencedNodeField.Type.IsAnyList())
                                {
                                    var argType = GetElementType(referencedNodeField.Type);

                                    this.WriteLine();
                                    this.WriteLine($"public new {baseType.Name} Add{StripPost(baseField.Name, "Opt")}{referencedNodeField.Name}(params {argType}[] items) => Add{StripPost(baseField.Name, "Opt")}{referencedNodeField.Name}Core(items);");
                                }
                            }
                        }
                    }
                }
            }

            this.CloseBlock();
        }
        else if (node is Node)
        {
            var nd = (Node)node;
            this.WriteComment($"<remarks>");
            this.WriteComment($"<para>This node is associated with the following syntax kinds:</para>");
            this.WriteComment($"<list type=\"bullet\">");

            foreach (var kind in nd.Kinds)
            {
                this.WriteComment($"<item><description><see cref=\"SyntaxKind.{kind.Name}\"/></description></item>");
            }

            this.WriteComment($"</list>");
            this.WriteComment($"</remarks>");
            this.WriteLine($"public sealed partial class {node.Name} : {node.Base}");
            this.OpenBlock();

            var valueFields = nd.Fields.Where(n => !IsNodeOrNodeList(n.Type)).ToList();
            var nodeFields = nd.Fields.Where(n => IsNodeOrNodeList(n.Type)).ToList();

            foreach (var field in nodeFields)
            {
                if (field.Type is not "SyntaxToken"
                    and not "SyntaxList<SyntaxToken>")
                {
                    if (field.IsSeparatedNodeList || field.Type == "SyntaxNodeOrTokenList")
                    {
                        this.WriteLine($"private SyntaxNode? {CamelCase(field.Name)};");
                    }
                    else
                    {
                        var type = GetFieldType(field, green: false);
                        this.WriteLine($"private {type} {CamelCase(field.Name)};");
                    }
                }
            }

            // write constructor
            this.WriteLine();
            this.WriteLine($"internal {node.Name}(InternalSyntax.{LanguageNames.This}SyntaxNode green, {LanguageNames.This}SyntaxNode? parent, int position)");
            this.WriteLine("  : base(green, parent, position)");
            this.OpenBlock();
            this.CloseBlock();
            this.WriteLine();

            // property accessors
            for (int i = 0, n = nodeFields.Count; i < n; i++)
            {
                var field = nodeFields[i];
                var directlyOverride = string.Compare(field.Override, "true", true) == 0;
                var overriddenName = field.IsOverride()
                    ? directlyOverride ? field.Name : field.Override
                    : field.Name;
                if (field.Type == "SyntaxToken")
                {
                    this.WriteComment(field.PropertyComment, "");
                    if (field.IsOverride() && !directlyOverride)
                    {
                        this.WriteLine($"public override {GetRedPropertyType(field)} {overriddenName} => this.{field.Name};");
                        this.Write($"public {GetRedPropertyType(field)} {field.Name}");
                    }
                    else
                        this.Write($"public {OverrideOrNewModifier(field)}{GetRedPropertyType(field)} {field.Name}");
                    if (field.IsOptional())
                    {
                        this.WriteLine();
                        this.OpenBlock();
                        this.WriteLine("get");
                        this.OpenBlock();
                        this.WriteLine($"var slot = ((Syntax.InternalSyntax.{node.Name})this.Green).{CamelCase(field.Name)};");
                        this.WriteLine($"return slot is not null ? new SyntaxToken(this, slot, {GetChildPosition(i)}, {GetChildIndex(i)}) : default;");
                        this.CloseBlock();
                        this.CloseBlock();
                    }
                    else
                    {
                        this.WriteLine($" => new SyntaxToken(this, ((Syntax.InternalSyntax.{node.Name})this.Green).{CamelCase(field.Name)}, {GetChildPosition(i)}, {GetChildIndex(i)});");
                    }
                }
                else if (field.Type == "SyntaxList<SyntaxToken>")
                {
                    this.WriteComment(field.PropertyComment, "");
                    if (field.IsOverride() && !directlyOverride)
                    {
                        this.WriteLine($"public override SyntaxTokenList {overriddenName} => this.{field.Name};");
                        this.Write($"public SyntaxTokenList {field.Name}");
                    }
                    else
                        this.WriteLine($"public {OverrideOrNewModifier(field)}SyntaxTokenList {field.Name}");
                    this.OpenBlock();
                    this.WriteLine("get");
                    this.OpenBlock();
                    this.WriteLine($"var slot = this.Green.GetSlot({i});");
                    this.WriteLine($"return slot is not null ? new SyntaxTokenList(this, slot, {GetChildPosition(i)}, {GetChildIndex(i)}) : default;");
                    this.CloseBlock();
                    this.CloseBlock();
                }
                else
                {
                    this.WriteComment(field.PropertyComment, "");
                    if (field.IsOverride() && !directlyOverride)
                    {
                        this.WriteLine($"public override {GetRedPropertyType(field)} {overriddenName} => this.{field.Name};");
                        this.Write($"public {GetRedPropertyType(field)} {field.Name}");
                    }
                    else
                        this.Write($"public {OverrideOrNewModifier(field)}{GetRedPropertyType(field)} {field.Name}");

                    if (field.IsNodeList)
                    {
                        this.WriteLine($" => new {field.Type}(GetRed(ref this.{CamelCase(field.Name)}, {i}));");
                    }
                    else if (field.IsSeparatedNodeList)
                    {
                        this.WriteLine();
                        this.OpenBlock();
                        this.WriteLine("get");
                        this.OpenBlock();

                        this.WriteLine($"var red = GetRed(ref this.{CamelCase(field.Name)}, {i});");
                        this.WriteLine($"return red is not null ? new {field.Type}(red, {GetChildIndex(i)}) : default;");
                        this.CloseBlock();
                        this.CloseBlock();
                    }
                    else if (field.Type == "SyntaxNodeOrTokenList")
                    {
                        throw new InvalidOperationException("field cannot be a random SyntaxNodeOrTokenList");
                    }
                    else
                    {
                        var suffix = field.IsOptional() ? "" : "!";
                        if (i == 0)
                        {
                            this.WriteLine($" => GetRedAtZero(ref this.{CamelCase(field.Name)}){suffix};");
                        }
                        else
                        {
                            this.WriteLine($" => GetRed(ref this.{CamelCase(field.Name)}, {i}){suffix};");
                        }
                    }
                }
                this.WriteLine();
            }

            foreach (var field in valueFields)
            {
                var directlyOverride = string.Compare(field.Override, "true", true) == 0;
                var overriddenName = field.IsOverride()
                    ? directlyOverride ? field.Name : field.Override
                    : field.Name;
                this.WriteComment(field.PropertyComment, "");
                if (field.IsOverride() && !directlyOverride)
                {
                    this.WriteLine($"public override {field.Type} {overriddenName} => this.{field.Name};");
                    this.WriteLine($"public {field.Type} {field.Name} => ((Syntax.InternalSyntax.{node.Name})this.Green).{field.Name};");
                }
                else
                    this.WriteLine($"public {OverrideOrNewModifier(field)}{field.Type} {field.Name} => ((Syntax.InternalSyntax.{node.Name})this.Green).{field.Name};");
                if (field.IsOverride() && !directlyOverride)
                    this.WriteLine($"public {field.Type} {field.Name} => ((Syntax.InternalSyntax.{node.Name})this.Green).{field.Name};");
                this.WriteLine();
            }

            {
                //GetNodeSlot forces creation of a red node.
                this.Write("internal override SyntaxNode? GetNodeSlot(int index)");

                var relevantNodes = nodeFields.Select((field, index) => (field, index))
                                              .Where(t => t.field.Type is not "SyntaxToken" and not "SyntaxList<SyntaxToken>");
                if (!relevantNodes.Any())
                {
                    this.WriteLine(" => null;");
                }
                else if (relevantNodes.Count() == 1)
                {
                    var (field, index) = relevantNodes.Single();
                    var whenTrue = index == 0
                        ? $"GetRedAtZero(ref this.{CamelCase(field.Name)})"
                        : $"GetRed(ref this.{CamelCase(field.Name)}, {index})";

                    var suffix = field.IsOptional() ? "" : "!";
                    this.WriteLine($" => index == {index} ? {whenTrue}{suffix} : null;");
                }
                else
                {
                    this.WriteLine();
                    Indent();
                    this.WriteLine("=> index switch");
                    this.OpenBlock();
                    foreach (var (field, index) in relevantNodes)
                    {
                        var suffix = field.IsOptional() ? "" : "!";
                        if (index == 0)
                        {
                            this.WriteLine($"{index} => GetRedAtZero(ref this.{CamelCase(field.Name)}){suffix},");
                        }
                        else
                        {
                            this.WriteLine($"{index} => GetRed(ref this.{CamelCase(field.Name)}, {index}){suffix},");
                        }
                    }
                    this.WriteLine("_ => null,");
                    this.CloseBlock(";");
                    Unindent();
                }
            }

            this.WriteLine();

            {
                //GetCachedSlot returns a red node if we have it.
                this.Write("internal override SyntaxNode? GetCachedSlot(int index)");

                var relevantNodes = nodeFields.Select((field, index) => (field, index))
                                              .Where(t => t.field.Type is not "SyntaxToken" and not "SyntaxList<SyntaxToken>");
                if (!relevantNodes.Any())
                {
                    this.WriteLine(" => null;");
                }
                else if (relevantNodes.Count() == 1)
                {
                    var (field, index) = relevantNodes.Single();
                    this.WriteLine($" => index == {index} ? this.{CamelCase(field.Name)} : null;");
                }
                else
                {
                    this.WriteLine();
                    Indent();
                    this.WriteLine("=> index switch");
                    this.OpenBlock();
                    foreach (var (field, index) in relevantNodes)
                    {
                        this.WriteLine($"{index} => this.{CamelCase(field.Name)},");
                    }
                    this.WriteLine("_ => null,");
                    this.CloseBlock(";");
                    Unindent();
                }
            }

            this.WriteRedAcceptMethods(nd);
            this.WriteRedUpdateMethod(nd);
            this.WriteRedWithMethods(nd);
            this.WriteRedListHelperMethods(nd);

            this.CloseBlock();
        }
    }

    private string GetRedFieldType(Field field)
    {
        if (field.Type == "SyntaxList<SyntaxToken>")
            return "SyntaxTokenList";

        if (field.IsOptional() && IsNode(field.Type) && field.Type != "SyntaxToken")
            return field.Type + "?";

        return field.Type;
    }

    private string GetChildPosition(int i)
        => i == 0 ? "Position" : "GetChildPosition(" + i + ")";

    private string GetChildIndex(int i)
        => i == 0 ? "0" : "GetChildIndex(" + i + ")";

