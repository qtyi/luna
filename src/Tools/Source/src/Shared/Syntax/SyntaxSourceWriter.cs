        this.WriteLine($"public override {(genericResult ? "TResult?" : "void")} Accept{genericArgs}({LanguageNames.This}SyntaxVisitor{genericArgs} visitor){(genericResult ? " where TResult : default" : "")} => visitor.Visit{StripPost(node.Name, "Syntax")}(this);");
    }

