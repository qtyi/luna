using System.Diagnostics;
using System.Xml;
using Microsoft.CodeAnalysis;
using Qtyi.CodeAnalysis;
using Luna.Compilers.Generators.CSharp;
using LanguageNames = Qtyi.CodeAnalysis.LanguageNames;

namespace Luna.Compilers.Generators.Syntax;

using Model;

internal class SyntaxSourceWriter : SyntaxFileWriter
{
    private SyntaxSourceWriter(TextWriter writer, SyntaxTree tree, CancellationToken cancellationToken)
        : base(writer, tree, cancellationToken) { }

    /// <summary>
    /// 写入红树节点的访问、重写和工厂方法。
    /// </summary>
    public static void WriteMain(TextWriter writer, SyntaxTree tree, Compilation _, CancellationToken cancellationToken) => new SyntaxSourceWriter(writer, tree, cancellationToken).WriteMain();

    /// <summary>
    /// 写入绿树节点的类型定义及访问、重写和上下文、静态工厂方法。
    /// </summary>
    public static void WriteInternal(TextWriter writer, SyntaxTree tree, Compilation _, CancellationToken cancellationToken) => new SyntaxSourceWriter(writer, tree, cancellationToken).WriteInternal();

    /// <summary>
    /// 写入红树节点的类型定义。
    /// </summary>
    public static void WriteSyntax(TextWriter writer, SyntaxTree tree, Compilation _, CancellationToken cancellationToken) => new SyntaxSourceWriter(writer, tree, cancellationToken).WriteSyntax();

    private void WriteFileHeader()
    {
        this.WriteLine("// <auto-generated />");
        this.WriteLine();
        this.WriteLine("#nullable enable");
        this.WriteLine();
        this.WriteLine("using System;");
        this.WriteLine("using System.Collections.Generic;");
        this.WriteLine("using System.Diagnostics.CodeAnalysis;");
        this.WriteLine("using Microsoft.CodeAnalysis;");
        this.WriteLine("using Microsoft.CodeAnalysis.Syntax.InternalSyntax;");
        this.WriteLine($"using Qtyi.CodeAnalysis.{LanguageNames.This};");
        this.WriteLine("using Roslyn.Utilities;");
        this.WriteLine();
    }

    private void WriteMain()
    {
        this.WriteFileHeader();
        this.WriteLine($"namespace Qtyi.CodeAnalysis.{LanguageNames.This}");
        this.OpenBlock();
        this.WriteLine("using System.Diagnostics.CodeAnalysis;");
        this.WriteLine("using Microsoft.CodeAnalysis;");
        this.WriteLine($"using Qtyi.CodeAnalysis.{LanguageNames.This}.Syntax;");
        this.WriteRedVisitors();
        this.WriteRedRewriter();
        this.WriteRedFactories();
        this.CloseBlock();
    }

    private void WriteInternal()
    {
        this.WriteFileHeader();
        this.WriteLine($"namespace Qtyi.CodeAnalysis.{LanguageNames.This}.Syntax.InternalSyntax");
        this.OpenBlock();
        this.WriteGreenTypes();
        this.WriteGreenVisitors();
        this.WriteGreenAccumulator();
        this.WriteGreenTraverser();
        this.WriteGreenRewriter();
        this.WriteContextualGreenFactories();
        this.WriteStaticGreenFactories();
        this.CloseBlock();
    }

    private void WriteSyntax()
    {
        this.WriteFileHeader();
        this.WriteLine($"namespace Qtyi.CodeAnalysis.{LanguageNames.This}.Syntax");
        this.OpenBlock();
        this.WriteLine("using Microsoft.CodeAnalysis;");
        this.WriteRedTypes();
        this.CloseBlock();
    }

    private void WriteGreenTypes()
    {
        var nodes = this.Tree.Types.Where(n => n is not PredefinedNode).ToList();
        foreach (var node in nodes)
        {
            this.WriteLine();
            this.WriteGreenType(node);
        }
    }

    private void WriteGreenType(SyntaxTreeType node)
    {
        this.WriteComment(node.TypeComment, "");

        if (node is AbstractNode)
        {
            var nd = (AbstractNode)node;
            this.WriteLine($"internal abstract partial class {node.Name} : {node.Base}");
            this.OpenBlock();

            // ctor with diagnostics and annotations
            this.WriteLine($"internal {node.Name}(SyntaxKind kind, DiagnosticInfo[]? diagnostics, SyntaxAnnotation[]? annotations)");
            this.WriteLine("  : base(kind, diagnostics, annotations)");
            this.OpenBlock();
            if (node.Name == "DirectiveTriviaSyntax")
            {
                this.WriteLine("this.flags |= NodeFlags.ContainsDirectives;");
            }
            this.CloseBlock();
            this.WriteLine();
            // ctor without diagnostics and annotations
            this.WriteLine($"internal {node.Name}(SyntaxKind kind)");
            this.WriteLine("  : base(kind)");
            this.OpenBlock();
            if (node.Name == "DirectiveTriviaSyntax")
            {
                this.WriteLine("this.flags |= NodeFlags.ContainsDirectives;");
            }
            this.CloseBlock();

            // object reader constructor
            this.WriteLine();
            this.WriteLine($"protected {node.Name}(ObjectReader reader)");
            this.WriteLine("  : base(reader)");
            this.OpenBlock();
            if (node.Name == "DirectiveTriviaSyntax")
            {
                this.WriteLine("this.flags |= NodeFlags.ContainsDirectives;");
            }
            this.CloseBlock();

            var valueFields = nd.Fields.Where(n => !IsNodeOrNodeList(n.Type)).ToList();
            var nodeFields = nd.Fields.Where(n => IsNodeOrNodeList(n.Type)).ToList();

            foreach (var field in nodeFields)
            {
                this.WriteLine();
                this.WriteComment(field.PropertyComment, "");

                if (field.IsSeparatedNodeList ||
                    field.IsNodeList)
                {
                    this.WriteLine($"public abstract {(field.IsNew() ? "new " : "")}Microsoft.CodeAnalysis.Syntax.InternalSyntax.{field.Type} {field.Name} {{ get; }}");
                }
                else
                {
                    this.WriteLine($"public abstract {(field.IsNew() ? "new " : "")}{(GetFieldType(field, green: true))} {field.Name} {{ get; }}");
                }
            }

            foreach (var field in valueFields)
            {
                this.WriteLine();
                this.WriteComment(field.PropertyComment, "");

                this.WriteLine($"public abstract {(field.IsNew() ? "new " : "")}{field.Type} {field.Name} {{ get; }}");
            }

            this.CloseBlock();
        }
        else if (node is Node)
        {
            var nd = (Node)node;

            this.WriteLine($"internal sealed partial class {node.Name} : {node.Base}");
            this.OpenBlock();

            var valueFields = nd.Fields.Where(n => !IsNodeOrNodeList(n.Type)).ToList();
            var nodeFields = nd.Fields.Where(n => IsNodeOrNodeList(n.Type)).ToList();

            foreach (var field in nodeFields)
            {
                var type = GetFieldType(field, green: true);
                this.WriteLine($"internal readonly {type} {CamelCase(field.Name)};");
            }

            foreach (var field in valueFields)
            {
                this.WriteLine($"internal readonly {field.Type} {CamelCase(field.Name)};");
            }

            // write constructor with diagnostics and annotations
            this.WriteLine();
            this.Write($"internal {node.Name}(SyntaxKind kind");

            this.WriteGreenNodeConstructorArgs(nodeFields, valueFields);

            this.WriteLine(", DiagnosticInfo[]? diagnostics, SyntaxAnnotation[]? annotations)");
            this.WriteLine("  : base(kind, diagnostics, annotations)");
            this.OpenBlock();
            this.WriteCtorBody(valueFields, nodeFields);
            this.CloseBlock();

            // write constructor with async
            this.WriteLine();
            this.Write($"internal {node.Name}(SyntaxKind kind");

            this.WriteGreenNodeConstructorArgs(nodeFields, valueFields);

            this.WriteLine(", SyntaxFactoryContext context)");
            this.WriteLine("  : base(kind)");
            this.OpenBlock();
            this.WriteLine("this.SetFactoryContext(context);");
            this.WriteCtorBody(valueFields, nodeFields);
            this.CloseBlock();

            // write constructor without diagnostics and annotations
            this.WriteLine();
            this.Write($"internal {node.Name}(SyntaxKind kind");

            this.WriteGreenNodeConstructorArgs(nodeFields, valueFields);

            this.WriteLine(")");
            this.WriteLine("  : base(kind)");
            this.OpenBlock();
            this.WriteCtorBody(valueFields, nodeFields);
            this.CloseBlock();
            this.WriteLine();

            // property accessors
            foreach (var field in nodeFields)
            {
                var directlyOverride = string.Compare(field.Override, "true", true) == 0;
                var overriddenName = field.IsOverride()
                    ? directlyOverride ? field.Name : field.Override
                    : field.Name;
                this.WriteComment(field.PropertyComment, "");
                if (field.IsNodeList)
                {
                    this.WriteLine($"public {OverrideOrNewModifier(field)}Microsoft.CodeAnalysis.Syntax.InternalSyntax.{field.Type} {overriddenName} => new Microsoft.CodeAnalysis.Syntax.InternalSyntax.{field.Type}(this.{CamelCase(field.Name)});");
                    if (field.IsOverride() && !directlyOverride)
                        this.WriteLine($"public Microsoft.CodeAnalysis.Syntax.InternalSyntax.{field.Type} {field.Name} => new Microsoft.CodeAnalysis.Syntax.InternalSyntax.{field.Type}(this.{CamelCase(field.Name)});");
                }
                else if (field.IsSeparatedNodeList)
                {
                    this.WriteLine($"public {OverrideOrNewModifier(field)}Microsoft.CodeAnalysis.Syntax.InternalSyntax.{field.Type} {overriddenName} => new Microsoft.CodeAnalysis.Syntax.InternalSyntax.{field.Type}(new Microsoft.CodeAnalysis.Syntax.InternalSyntax.SyntaxList<{LanguageNames.This}SyntaxNode>(this.{CamelCase(field.Name)}));");
                    if (field.IsOverride() && !directlyOverride)
                        this.WriteLine($"public Microsoft.CodeAnalysis.Syntax.InternalSyntax.{field.Type} {field.Name} => new Microsoft.CodeAnalysis.Syntax.InternalSyntax.{field.Type}(new Microsoft.CodeAnalysis.Syntax.InternalSyntax.SyntaxList<{LanguageNames.This}SyntaxNode>(this.{CamelCase(field.Name)}));");
                }
                else if (field.Type == "SyntaxNodeOrTokenList")
                {
                    this.WriteLine($"public {OverrideOrNewModifier(field)}Microsoft.CodeAnalysis.Syntax.InternalSyntax.SyntaxList<{LanguageNames.This}SyntaxNode> {overriddenName} => new Microsoft.CodeAnalysis.Syntax.InternalSyntax.SyntaxList<{LanguageNames.This}SyntaxNode>(this.{CamelCase(field.Name)});");
                    if (field.IsOverride() && !directlyOverride)
                        this.WriteLine($"public Microsoft.CodeAnalysis.Syntax.InternalSyntax.SyntaxList<{LanguageNames.This}SyntaxNode> {field.Name} => new Microsoft.CodeAnalysis.Syntax.InternalSyntax.SyntaxList<{LanguageNames.This}SyntaxNode>(this.{CamelCase(field.Name)});");
                }
                else
                {
                    this.WriteLine($"public {OverrideOrNewModifier(field)}{(GetFieldType(field, green: true))} {overriddenName} => this.{CamelCase(field.Name)};");
                    if (field.IsOverride() && !directlyOverride)
                        this.WriteLine($"public {(GetFieldType(field, green: true))} {field.Name} => this.{CamelCase(field.Name)};");
                }
            }

            foreach (var field in valueFields)
            {
                bool directlyOverride = string.Compare(field.Override, "true", true) == 0;
                var overriddenName = field.IsOverride()
                    ? directlyOverride ? field.Name : field.Override
                    : field.Name;
                this.WriteComment(field.PropertyComment, "");
                this.WriteLine($"public {OverrideOrNewModifier(field)}{field.Type} {overriddenName} => this.{CamelCase(field.Name)};");
                if (field.IsOverride() && !directlyOverride)
                    this.WriteLine($"public {field.Type} {field.Name} => this.{CamelCase(field.Name)};");
            }

            // GetSlot
            this.WriteLine();
            this.Write("internal override GreenNode? GetSlot(int index)");

            if (nodeFields.Count == 0)
            {
                this.WriteLine(" => null;");
            }
            else if (nodeFields.Count == 1)
            {
                this.WriteLine();
                Indent();
                this.WriteLine($"=> index == 0 ? this.{CamelCase(nodeFields[0].Name)} : null;");
                Unindent();
            }
            else
            {
                this.WriteLine();
                Indent();
                this.WriteLine("=> index switch");
                this.OpenBlock();
                for (int i = 0, n = nodeFields.Count; i < n; i++)
                {
                    var field = nodeFields[i];
                    this.WriteLine($"{i} => this.{CamelCase(field.Name)},");
                }
                this.WriteLine("_ => null,");
                this.CloseBlock(";");
                Unindent();
            }

            this.WriteLine();
            this.WriteLine($"internal override SyntaxNode CreateRed(SyntaxNode? parent, int position) => new {LanguageNames.This}.Syntax.{node.Name}(this, parent as {LanguageNames.This}.{LanguageNames.This}SyntaxNode, position);");

            this.WriteGreenAcceptMethods(nd);
            this.WriteGreenUpdateMethod(nd);
            this.WriteSetDiagnostics(nd);
            this.WriteSetAnnotations(nd);

            this.WriteGreenSerialization(nd);
            this.CloseBlock();
        }
    }

    private void WriteGreenNodeConstructorArgs(List<Field> nodeFields, List<Field> valueFields)
    {
        foreach (var field in nodeFields)
        {
            this.Write($", {(GetFieldType(field, green: true))} {CamelCase(field.Name)}");
        }

        foreach (var field in valueFields)
        {
            this.Write($", {field.Type} {CamelCase(field.Name)}");
        }
    }

    private void WriteGreenSerialization(Node node)
    {
        var valueFields = node.Fields.Where(n => !IsNodeOrNodeList(n.Type)).ToList();
        var nodeFields = node.Fields.Where(n => IsNodeOrNodeList(n.Type)).ToList();

        // object reader constructor
        this.WriteLine();
        this.WriteLine($"internal {node.Name}(ObjectReader reader)");
        this.WriteLine("  : base(reader)");
        this.OpenBlock();
        this.WriteLine($"this.SlotCount = {nodeFields.Count};");

        foreach (var field in nodeFields)
        {
            string type = GetFieldType(field, green: true);
            this.WriteLine($"var {CamelCase(field.Name)} = ({type})reader.ReadValue();");

            if (field.Type.IsAnyList() || field.IsOptional())
            {
                this.WriteLine($"if ({CamelCase(field.Name)} is not null)");
                this.OpenBlock();
                this.WriteLine($"AdjustFlagsAndWidth({CamelCase(field.Name)});");
                this.WriteLine($"this.{CamelCase(field.Name)} = {CamelCase(field.Name)};");
                this.CloseBlock();
            }
            else
            {
                this.WriteLine($"AdjustFlagsAndWidth({CamelCase(field.Name)});");
                this.WriteLine($"this.{CamelCase(field.Name)} = {CamelCase(field.Name)};");
            }
        }

        foreach (var field in valueFields)
        {
            this.WriteLine($"this.{CamelCase(field.Name)} = ({(GetFieldType(field, green: true))})reader.{(GetReaderMethod(GetFieldType(field, green: true)))}();");
        }

        this.CloseBlock();

        // IWritable
        this.WriteLine();
        this.WriteLine("internal override void WriteTo(ObjectWriter writer)");
        this.OpenBlock();
        this.WriteLine("base.WriteTo(writer);");

        foreach (var field in nodeFields)
        {
            this.WriteLine($"writer.WriteValue(this.{CamelCase(field.Name)});");
        }

        foreach (var field in valueFields)
        {
            var type = GetFieldType(field, green: true);
            this.WriteLine($"writer.{GetWriterMethod(type)}(this.{CamelCase(field.Name)});");
        }

        this.CloseBlock();

        // IReadable
        this.WriteLine();
        this.WriteLine($"static {node.Name}()");
        this.OpenBlock();
        this.WriteLine($"ObjectBinder.RegisterTypeReader(typeof({node.Name}), r => new {node.Name}(r));");
        this.CloseBlock();
    }

    private string GetWriterMethod(string type)
        => type switch
        {
            "bool" => "WriteBoolean",
            _ => throw new InvalidOperationException($"Type '{type}' not supported for object reader serialization."),
        };

    private string GetReaderMethod(string type)
        => type switch
        {
            "bool" => "ReadBoolean",
            _ => throw new InvalidOperationException($"Type '{type}' not supported for object reader serialization."),
        };

    private void WriteCtorBody(List<Field> valueFields, List<Field> nodeFields)
    {
        // constructor body
        this.WriteLine($"this.SlotCount = {nodeFields.Count};");

        foreach (var field in nodeFields)
        {
            if (field.Type.IsAnyList() || field.IsOptional())
            {
                this.WriteLine($"if ({CamelCase(field.Name)} is not null)");
                this.OpenBlock();
                this.WriteLine($"this.AdjustFlagsAndWidth({CamelCase(field.Name)});");
                this.WriteLine($"this.{CamelCase(field.Name)} = {CamelCase(field.Name)};");
                this.CloseBlock();
            }
            else
            {
                this.WriteLine($"this.AdjustFlagsAndWidth({CamelCase(field.Name)});");
                this.WriteLine($"this.{CamelCase(field.Name)} = {CamelCase(field.Name)};");
            }
        }

        foreach (var field in valueFields)
        {
            this.WriteLine($"this.{CamelCase(field.Name)} = {CamelCase(field.Name)};");
        }
    }

    private void WriteSetAnnotations(Node node)
    {
        this.WriteLine();
        this.WriteLine("internal override GreenNode SetAnnotations(SyntaxAnnotation[]? annotations)");
        this.Write($"    => new {node.Name}(");
        this.Write(CommaJoin(
            "this.Kind",
            node.Fields.Select(f => $"this.{CamelCase(f.Name)}"),
            "GetDiagnostics()",
            "annotations"));
        this.WriteLine(");");
    }

    private void WriteSetDiagnostics(Node node)
    {
        this.WriteLine();
        this.WriteLine("internal override GreenNode SetDiagnostics(DiagnosticInfo[]? diagnostics)");
        this.Write($"    => new {node.Name}(");
        this.Write(CommaJoin(
            "this.Kind",
            node.Fields.Select(f => $"this.{CamelCase(f.Name)}"),
            "diagnostics",
            "GetAnnotations()"));
        this.WriteLine(");");
    }

    private void WriteGreenAcceptMethods(Node node)
    {
        this.WriteLine();
        this.WriteLine($"public override void Accept({LanguageNames.This}SyntaxVisitor visitor) => visitor.Visit{StripPost(node.Name, "Syntax")}(this);");
        this.WriteLine($"public override TResult? Accept<TResult>({LanguageNames.This}SyntaxVisitor<TResult> visitor) where TResult : default => visitor.Visit{StripPost(node.Name, "Syntax")}(this);");
    }

    private void WriteGreenVisitors()
    {
        this.WriteGreenVisitor(withResult: true);
        this.WriteGreenVisitor(withResult: false);
    }

    private void WriteGreenVisitor(bool withResult)
    {
        var nodes = this.Tree.Types.Where(n => n is not PredefinedNode).ToList();

        this.WriteLine();
        this.WriteLine($"partial class {LanguageNames.This}SyntaxVisitor" + (withResult ? "<TResult>" : ""));
        this.OpenBlock();
        foreach (var node in nodes.OfType<Node>())
        {
            this.WriteLine($"public virtual {(withResult ? "TResult?" : "void")} Visit{StripPost(node.Name, "Syntax")}({node.Name} node) => this.DefaultVisit(node);");
        }
        this.CloseBlock();
    }

    private void WriteGreenUpdateMethod(Node node)
    {
        this.WriteLine();
        this.Write($"public {node.Name} Update(");
        this.Write(CommaJoin(node.Fields.Select(f =>
        {
            var type =
                f.Type == "SyntaxNodeOrTokenList" ? $"Microsoft.CodeAnalysis.Syntax.InternalSyntax.SyntaxList<{LanguageNames.This}SyntaxNode>" :
                f.Type == "SyntaxTokenList" ? "Microsoft.CodeAnalysis.Syntax.InternalSyntax.SyntaxList<SyntaxToken>" :
                f.IsNodeList ? "Microsoft.CodeAnalysis.Syntax.InternalSyntax." + f.Type :
                f.IsSeparatedNodeList ? "Microsoft.CodeAnalysis.Syntax.InternalSyntax." + f.Type :
                f.Type + (f.IsOptional() ? "?" : string.Empty);

            return $"{type} {CamelCase(f.Name)}";
        })));
        this.WriteLine(")");
        this.OpenBlock();

        this.Write("if (");
        int nCompared = 0;
        foreach (var field in node.Fields)
        {
            if (IsDerivedOrListOfDerived("SyntaxNode", field.Type) || IsDerivedOrListOfDerived("SyntaxToken", field.Type) || field.Type == "SyntaxNodeOrTokenList")
            {
                if (nCompared > 0)
                    this.Write(" || ");
                this.Write($"{CamelCase(field.Name)} != this.{field.Name}");
                nCompared++;
            }
        }
        if (nCompared > 0)
        {
            this.WriteLine(")");
            this.OpenBlock();
            this.Write($"var newNode = SyntaxFactory.{StripPost(node.Name, "Syntax")}(");
            this.Write(CommaJoin(
                node.Kinds.Count > 1 ? "this.Kind" : "",
                node.Fields.Select(f => CamelCase(f.Name))));
            this.WriteLine(");");
            this.WriteLine("var diags = GetDiagnostics();");
            this.WriteLine("if (diags?.Length > 0)");
            this.WriteLine("    newNode = newNode.WithDiagnosticsGreen(diags);");
            this.WriteLine("var annotations = GetAnnotations();");
            this.WriteLine("if (annotations?.Length > 0)");
            this.WriteLine("    newNode = newNode.WithAnnotationsGreen(annotations);");
            this.WriteLine("return newNode;");
            this.CloseBlock();
        }

        this.WriteLine();
        this.WriteLine("return this;");
        this.CloseBlock();
    }

    private void WriteGreenAccumulator()
    {
        var nodes = this.Tree.Types.Where(n => n is not PredefinedNode).ToList();

        this.WriteLine();
        this.WriteLine($"partial class {LanguageNames.This}SyntaxAccumulator<TResult> : {LanguageNames.This}SyntaxVisitor<IEnumerable<TResult>>");
        this.OpenBlock();
        int nWritten = 0;
        foreach (var node in nodes.OfType<Node>())
        {
            var nodeFields = node.Fields.Where(nd => IsNodeOrNodeList(nd.Type)).ToList();

            if (nWritten > 0)
                this.WriteLine();
            nWritten++;
            this.WriteLine($"public override IEnumerable<TResult> Visit{StripPost(node.Name, "Syntax")}({node.Name} node)");
            this.OpenBlock();

            if (nodeFields.Count == 0)
            {
                this.WriteLine("yield break;");
            }
            else
            {
                foreach (var f in node.Fields)
                {
                    string visitMethod;
                    if (f.Type.IsAnyList())
                        visitMethod = "VisitList";
                    else if (f.Type == "SyntaxToken")
                        visitMethod = "Visit";
                    else if (IsNode(f.Type))
                        visitMethod = "Visit";
                    else
                        continue;
                    if (!f.Type.IsAnyList() && f.IsOptional())
                        this.Write($"if (node.{f.Name} is not null) ");
                    this.WriteLine($"foreach (var result in this.{visitMethod}(node.{f.Name})) yield return result;");
                }
            }

            this.CloseBlock();
        }

        this.CloseBlock();
    }

    private void WriteGreenTraverser()
    {
        var nodes = this.Tree.Types.Where(n => n is not PredefinedNode).ToList();

        this.WriteLine();
        this.WriteLine($"partial class {LanguageNames.This}SyntaxTraverser : {LanguageNames.This}SyntaxVisitor");
        this.OpenBlock();
        int nWritten = 0;
        foreach (var node in nodes.OfType<Node>())
        {
            var nodeFields = node.Fields.Where(nd => IsNodeOrNodeList(nd.Type)).ToList();

            if (nWritten > 0)
                this.WriteLine();
            nWritten++;
            this.WriteLine($"public override void Visit{StripPost(node.Name, "Syntax")}({node.Name} node)");
            this.OpenBlock();

            if (nodeFields.Count != 0)
            {
                foreach (var f in node.Fields)
                {
                    string visitMethod;
                    if (f.Type.IsAnyList())
                        visitMethod = "VisitList";
                    else if (f.Type == "SyntaxToken")
                        visitMethod = "Visit";
                    else if (IsNode(f.Type))
                        visitMethod = "Visit";
                    else
                        continue;
                    if (!f.Type.IsAnyList() && f.IsOptional())
                        this.Write($"if (node.{f.Name} is not null) ");
                    this.WriteLine($"this.{visitMethod}(node.{f.Name});");
                }
            }

            this.CloseBlock();
        }

        this.CloseBlock();
    }

    private void WriteGreenRewriter()
    {
        var nodes = this.Tree.Types.Where(n => n is not PredefinedNode).ToList();

        this.WriteLine();
        this.WriteLine($"partial class {LanguageNames.This}SyntaxRewriter : {LanguageNames.This}SyntaxVisitor<{LanguageNames.This}SyntaxNode>");
        this.OpenBlock();
        int nWritten = 0;
        foreach (var node in nodes.OfType<Node>())
        {
            var nodeFields = node.Fields.Where(nd => IsNodeOrNodeList(nd.Type)).ToList();

            if (nWritten > 0)
                this.WriteLine();
            nWritten++;
            this.WriteLine($"public override {LanguageNames.This}SyntaxNode Visit{StripPost(node.Name, "Syntax")}({node.Name} node)");
            Indent();

            if (nodeFields.Count == 0)
            {
                this.WriteLine("=> node;");
            }
            else
            {
                this.Write("=> node.Update(");
                this.Write(CommaJoin(node.Fields.Select(f =>
                {
                    if (f.Type.IsAnyList())
                        return $"this.VisitList(node.{f.Name})";
                    else if (IsNode(f.Type))
                        return $"({f.Type}{(f.IsOptional() ? "?" : string.Empty)})this.Visit(node.{f.Name}){(!f.IsOptional() ? "!" : string.Empty)}";
                    else
                        return $"node.{f.Name}";
                })));
                this.WriteLine(");");
            }

            Unindent();
        }

        this.CloseBlock();
    }

    private void WriteContextualGreenFactories()
    {
        var nodes = this.Tree.Types.Where(n => n is not PredefinedNode and not AbstractNode).ToList();
        this.WriteLine();
        this.WriteLine("partial class ContextAwareSyntax");
        this.OpenBlock();
        this.WriteLine();
        this.WriteLine("private SyntaxFactoryContext context;");

        this.WriteLine();
        this.WriteLine("public ContextAwareSyntax(SyntaxFactoryContext context)");
        this.WriteLine("    => this.context = context;");

        this.WriteGreenFactories(nodes, withSyntaxFactoryContext: true);
        this.CloseBlock();
    }

    private void WriteStaticGreenFactories()
    {
        var nodes = this.Tree.Types.Where(n => n is not PredefinedNode and not AbstractNode).ToList();
        this.WriteLine();
        this.WriteLine("static partial class SyntaxFactory");
        this.OpenBlock();
        this.WriteGreenFactories(nodes);
        this.WriteGreenTypeList();
        this.CloseBlock();
    }

    private void WriteGreenFactories(List<SyntaxTreeType> nodes, bool withSyntaxFactoryContext = false)
    {
        foreach (var node in nodes.OfType<Node>())
        {
            this.WriteLine();
            this.WriteGreenFactory(node, withSyntaxFactoryContext);
        }
    }

    private void WriteGreenTypeList()
    {
        this.WriteLine();
        this.WriteLine("internal static IEnumerable<Type> GetNodeTypes()");
        Indent();
        this.WriteLine("=> new Type[]");
        this.OpenBlock();

        var nodes = this.Tree.Types.Where(n => n is not PredefinedNode and not AbstractNode).ToList();
        foreach (var node in nodes)
        {
            this.WriteLine($"typeof({node.Name}),");
        }

        this.CloseBlock(";");
        Unindent();
    }

    private void WriteGreenFactory(Node nd, bool withSyntaxFactoryContext = false)
    {
        var valueFields = nd.Fields.Where(n => !IsNodeOrNodeList(n.Type)).ToList();
        var nodeFields = nd.Fields.Where(n => IsNodeOrNodeList(n.Type)).ToList();

        this.Write($"public {(withSyntaxFactoryContext ? "" : "static ")}{nd.Name} {StripPost(nd.Name, "Syntax")}(");
        this.WriteGreenFactoryParameters(nd);
        this.WriteLine(")");
        this.OpenBlock();

        // validate kind
        if (nd.Kinds.Count >= 2)
        {
            this.WriteLine("switch (kind)");
            this.OpenBlock();
            var kinds = nd.Kinds.Distinct().ToList();
            foreach (var kind in kinds)
            {
                this.WriteLine($"case SyntaxKind.{kind.Name}:{(kind == kinds.Last() ? " break;" : "")}");
            }
            this.WriteLine("default: throw new ArgumentException(nameof(kind));");
            this.CloseBlock();
        }

        // validate parameters
        this.WriteLineWithoutIndent("#if DEBUG");
        foreach (var field in nodeFields)
        {
            var pname = CamelCase(field.Name);

            if (!field.Type.IsAnyList() && !field.IsOptional())
            {
                this.WriteLine($"if ({CamelCase(field.Name)} is null) throw new ArgumentNullException(nameof({CamelCase(field.Name)}));");
            }
            if (field.Type == "SyntaxToken" && field.Kinds is not null && field.Kinds.Count > 0)
            {
                if (field.IsOptional())
                {
                    this.WriteLine($"if ({CamelCase(field.Name)} is not null)");
                    this.OpenBlock();
                }

                if (field.Kinds.Count == 1 && !field.IsOptional())
                {
                    this.WriteLine($"if ({pname}.Kind != SyntaxKind.{field.Kinds[0].Name}) throw new ArgumentException(nameof({pname}));");
                }
                else
                {
                    this.WriteLine($"switch ({pname}.Kind)");
                    this.OpenBlock();
                    var kinds = field.Kinds.Distinct().ToList();

                    //we need to check for Kind=None as well as node is null because that's what the red factory will pass
                    if (field.IsOptional())
                    {
                        kinds.Add(new Kind { Name = "None" });
                    }
                    foreach (var kind in kinds)
                    {
                        this.WriteLine($"case SyntaxKind.{kind.Name}:{(kind == kinds.Last() ? " break;" : "")}");
                    }

                    this.WriteLine($"default: throw new ArgumentException(nameof({pname}));");
                    this.CloseBlock();
                }

                if (field.IsOptional())
                {
                    this.CloseBlock();
                }
            }
        }

        this.WriteLineWithoutIndent("#endif");

        if (nd.Name != "SkippedTokensTriviaSyntax" &&
            nd.Name != "DocumentationCommentTriviaSyntax" &&
            nd.Name != "IncompleteMemberSyntax" &&
            valueFields.Count + nodeFields.Count <= 3)
